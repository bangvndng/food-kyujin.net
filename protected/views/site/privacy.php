<p>以下の場合は、第三者による個人情報の取得に関し、われわれは何らの責任を負いません。</p>
<p>
(1) ユーザー自らが 本サイト上の機能または別の手段を用いて他のユーザーに個人情報を明らかにする場合<br />
(2) 活動情報またはその他のユーザーが本サイト上に入力した情報により、期せずして本人が特定できてしまった場合<br />
(3) ユーザーご本人以外のユーザー個人を識別できる情報(ID・パスワード等)を入手した場合<br />
</p>

