<h2>個人情報の取り扱いについて</h2>
<p>われわれは本サイトをご利用になるみなさまの個人情報（以下の定義に従います）が重要なプライバシー情報と認識し、細心の注意を払い、以下の方針にもとづいて適正な管理・運用を行っていますので、本サイトをご利用いただく前に必ずお読みください。本サイトにおいて個人情報をご登録された場合には、個人情報の取扱いに関して同意をいただいたものといたします。</p>


<h2>１．個人情報 </h2>
<p>個人情報とは、ユーザー個人の関する情報であって、その情報を構成する氏名、住所、電話番号、メールアドレス、勤務先、生年月日その他の書き込み等によりユーザー個人を識別できるものも個人情報に含まれます。</p>


<h2>２．個人情報の取得と目的</h2>
<p>個人情報の取得と利用の目的及び活用範囲は以下の通りです。</p>
<p>
(1) 本サイトにおけるユーザーへのサービス提供と会員サービスにおける個人認証<br />
(2) ユーザーの任意に基づくプロフィールの作成<br />
(3) emailでの連絡、お問い合わせへの返答<br />
(4) アンケート、キャンペーンへの応募、プレゼント発送等<br />
</p>

<h2>３．個人情報を提供いただかなかった場合に生じる結果について</h2>
<p>本サイト内の各サービスの実施において、それぞれ必要となる個人情報項目を入力いただかない場合は、各々のサービスを受けられないことがあります。</p>


<h2>４．個人情報の第三者への開示</h2>
<p>ユーザーの個人情報について、ユーザーご本人の同意を得ずに第三者に開示することは、原則いたしません。提供先・提供情報内容を特定した上で、ユーザーの同意を得た場合に限り開示します。但し以下の場合は、関係法令に反しない範囲で、ユーザーの同意なくユーザーの個人情報を開示することがあります。</p>
<p>
(1) ユーザーが第三者に不利益を及ぼすと判断した場合<br />
(2) 裁判所、検察庁、警察、弁護士会、消費者センターまたはこれらに準じた権限を有する機関から、個人情報についての開示を求められた場合<br />
(3) ユーザーご本人から明示的に第三者への開示または提供を求められた場合 <br />
(4) 法令により開示または提供が許容されている場合<br />
(5) その他ユーザーご本人へサービスを提供するために必要であるとわれわれが合理的に判断した場合<br />
</p>

<h2>５．第三者の範囲</h2>
<p>以下の場合に、個人情報の提供を受けるものは、第三者に該当しないものとします。</p>
<p>
(1) われわれが利用目的の達成に必要な範囲内において個人情報の取扱いに全部または一部を委託する場合<br />
(2) その他の事由による事業の承継に伴って個人情報が提供される場合
</p>

